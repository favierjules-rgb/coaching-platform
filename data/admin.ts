// Compteur global : le même exercice (même order + même nom) revient
// souvent d'une semaine à l'autre dans un programme, un id dérivé de
// order+nom seul entrerait donc en collision entre séances (clés React
// dupliquées dès qu'une vue agrège des exercices de plusieurs séances,
// ex: l'analyse de programme filtrée par groupe musculaire).
let exerciseIdCounter = 0;

function exercise(
  order: number,
  name: string,
  sets: number,
  reps: string,
  restSeconds: number,
  tempo: string,
  recommendedLoad: string,
  muscleGroup: MuscleGroup,
  notes = "",
  videoUrl = "https://videos.seth-coaching.mock/exercices/demo.mp4",
): AdminExercise {
  exerciseIdCounter += 1;
  return {
    id: `ex-${exerciseIdCounter}-${name.toLowerCase().replace(/[^a-z0-9]+/g, "-")}`,
    order,
    name,
    sets,
    reps,
    restSeconds,
    tempo,
    recommendedLoad,
    muscleGroup,
    videoUrl,
    notes,
  };
}

function session(
  id: string,
  weekNumber: number,
  day: string,
  name: string,
  muscleGroup: string,
  durationMinutes: number,
  warmup: string,
  coachNotes: string,
  exercises: AdminExercise[],
): AdminWorkoutSession {
  return {
    id,
    programId: "",
    weekNumber,
    day,
    isRestDay: false,
    name,
    muscleGroup,
    durationMinutes,
    warmup,
    coachNotes,
    exercises,
  };
}

function restDay(id: string, weekNumber: number, day: string): AdminWorkoutSession {
  return {
    id,
    programId: "",
    weekNumber,
    day,
    isRestDay: true,
    name: "Repos",
    muscleGroup: "",
    durationMinutes: 0,
    warmup: "",
    coachNotes: "",
    exercises: [],
  };
}

function withProgramId(programId: string, sessions: AdminWorkoutSession[]): AdminWorkoutSession[] {
  return sessions.map((s) => ({ ...s, programId }));
}

