function meal(
  id: string,
  slot: AdminMeal["slot"],
  name: string,
  items: { name: string; quantity: string }[],
  calories: number,
  protein: number,
  carbs: number,
  fat: number,
  coachNotes = "",
): AdminMeal {
  return { id, slot, name, items, calories, protein, carbs, fat, coachNotes };
}

function nutritionDay(planId: string, day: string, meals: AdminMeal[]): AdminNutritionDay {
  return { id: `${planId}-${day.toLowerCase()}`, planId, day, meals };
}

