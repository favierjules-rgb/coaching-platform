function exercise(
  order: number,
  name: string,
  sets: number,
  reps: string,
  restSeconds: number,
  tempo: string,
  recommendedLoad: string,
  notes = "",
): AdminExercise {
  return {
    id: `ex-${order}-${name.toLowerCase().replace(/[^a-z0-9]+/g, "-")}`,
    order,
    name,
    sets,
    reps,
    restSeconds,
    tempo,
    recommendedLoad,
    videoUrl: "https://videos.seth-coaching.mock/exercices/demo.mp4",
    notes,
  };
}

function session(
  id: string,
  weekNumber: number,
  day: string,
  name: string,
  muscleGroup: string,
  durationMinutes: number,
  warmup: string,
  coachNotes: string,
  exercises: AdminExercise[],
): AdminWorkoutSession {
  return {
    id,
    programId: "",
    weekNumber,
    day,
    isRestDay: false,
    name,
    muscleGroup,
    durationMinutes,
    warmup,
    coachNotes,
    exercises,
  };
}

function restDay(id: string, weekNumber: number, day: string): AdminWorkoutSession {
  return {
    id,
    programId: "",
    weekNumber,
    day,
    isRestDay: true,
    name: "Repos",
    muscleGroup: "",
    durationMinutes: 0,
    warmup: "",
    coachNotes: "",
    exercises: [],
  };
}

function withProgramId(programId: string, sessions: AdminWorkoutSession[]): AdminWorkoutSession[] {
  return sessions.map((s) => ({ ...s, programId }));
}

